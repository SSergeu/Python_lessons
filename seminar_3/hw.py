def zad3(): # Создайте файл. Запишите в него N первых элементов последовательности Фибоначчи
    N = int(input('Введите число: '))
    result_list = []
    if N <= 2:
        for i in range(N):
            result_list.append(1)               
    else: # заполняем результирующий список нужными значениями
        for i in range(2):
            result_list.append(1)
        for i in range(N-2):
            result_list.append(result_list[i]+result_list[i+1])
    file = open('text_zad3.txt', 'w') # открываем файл для записи 'w'
    file.write(str(result_list)) # записываем в файл получившийся в ходе работы программы список
    file.close() # закрываем файл 

def zad4(): # В файле находятся названия фруктов.
    # Выведите все фрукты, названия которых начинаются на заданную букву.
    letter = str(input('Введите букву: '))  # считываем букву 
    
    file = open('text_zad4.txt', encoding='utf-8') # открываем файл со списком фруктов 
    words = file.readlines()                       # заполняем список значениями из файла
    for i in range(len(words)):
        if words[i][0]==letter:                    # сравниваем букву с первыми буквами элементов из списка
            print(words[i], end = '')
    file.close()                                   # закрываем файл 
    
