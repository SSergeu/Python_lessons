def zad2(): # Напишите программу, которая найдёт произведение пар чисел списка. 
    # Парой считаем первый и последний элемент, второй и предпоследний и т.д.
    list1 = [2, 3, 4, 5, 6] # исходный список 
    result_lust = [] # создаём результирующий список (пока что пустой)
    i = 0 # счётчик 
    n = len(list1)-1 # обрытное счётчику
    while i <= n: 
        result_lust.append(list1[i] * list1[n])  # заполняем результирующий список нужными значениями 
        i+=1
        n-=1
    print(result_lust)

